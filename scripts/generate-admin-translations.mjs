import fs from "node:fs";
import path from "node:path";

const sourceRoot = path.resolve("inventory-web/src");
const outputFile = path.join(sourceRoot, "i18n/generatedTranslations.ts");
const extensions = new Set([".ts", ".tsx"]);
const arabicRun = /[\u0600-\u06ff][\u0600-\u06ff\u0750-\u077f\u08a0-\u08ff\s،؛؟«»ـًٌٍَُِّْ\-–—:()/.+%0-9٠-٩]*/g;

function collectFiles(directory, output = []) {
  for (const entry of fs.readdirSync(directory, { withFileTypes: true })) {
    const fullPath = path.join(directory, entry.name);
    if (entry.isDirectory()) collectFiles(fullPath, output);
    else if (extensions.has(path.extname(fullPath)) && !fullPath.includes(`${path.sep}i18n${path.sep}`)) output.push(fullPath);
  }
  return output;
}

const phrases = new Set();
for (const file of collectFiles(sourceRoot)) {
  const source = fs.readFileSync(file, "utf8");
  for (const match of source.matchAll(arabicRun)) {
    const phrase = match[0].trim().replace(/\s+/g, " ");
    if (phrase.length > 1 && phrase.length <= 500) phrases.add(phrase);
  }
}

const sortedPhrases = [...phrases].sort((a, b) => b.length - a.length);

function makeBatches(items) {
  const batches = [];
  let batch = [];
  let length = 0;
  for (const item of items) {
    if (batch.length >= 18 || length + item.length > 2400) {
      batches.push(batch);
      batch = [];
      length = 0;
    }
    batch.push(item);
    length += item.length + 20;
  }
  if (batch.length) batches.push(batch);
  return batches;
}

async function translateBatch(batch, targetLanguage) {
  const marked = batch.map((text, index) => `__T${index}__ ${text}`).join("\n");
  const url = new URL("https://translate.googleapis.com/translate_a/single");
  url.searchParams.set("client", "gtx");
  url.searchParams.set("sl", "ar");
  url.searchParams.set("tl", targetLanguage);
  url.searchParams.set("dt", "t");
  url.searchParams.set("q", marked);

  const response = await fetch(url);
  if (!response.ok) throw new Error(`Translation request failed: ${response.status}`);
  const payload = await response.json();
  const translated = payload[0].map((part) => part[0]).join("");
  const result = new Array(batch.length).fill("");
  const marker = /__T(\d+)__\s*/g;
  const matches = [...translated.matchAll(marker)];
  for (let i = 0; i < matches.length; i++) {
    const index = Number(matches[i][1]);
    const start = matches[i].index + matches[i][0].length;
    const end = matches[i + 1]?.index ?? translated.length;
    result[index] = translated.slice(start, end).trim();
  }
  return result;
}

async function translateAll(targetLanguage) {
  const translations = {};
  const batches = makeBatches(sortedPhrases);
  for (let index = 0; index < batches.length; index++) {
    const batch = batches[index];
    let translated;
    for (let attempt = 1; attempt <= 4; attempt++) {
      try {
        translated = await translateBatch(batch, targetLanguage);
        if (translated.every(Boolean)) break;
      } catch (error) {
        if (attempt === 4) throw error;
      }
      await new Promise((resolve) => setTimeout(resolve, attempt * 800));
    }
    batch.forEach((source, itemIndex) => {
      translations[source] = translated[itemIndex] || source;
    });
    if ((index + 1) % 20 === 0) {
      console.log(`${targetLanguage}: ${index + 1}/${batches.length} batches`);
    }
    await new Promise((resolve) => setTimeout(resolve, 80));
  }
  return translations;
}

const english = await translateAll("en");
const persian = await translateAll("fa");
fs.mkdirSync(path.dirname(outputFile), { recursive: true });
fs.writeFileSync(
  outputFile,
  `// Generated by scripts/generate-admin-translations.mjs.\n` +
  `// Regenerate after adding new Arabic administration text.\n` +
  `export const englishTranslations: Record<string, string> = ${JSON.stringify(english, null, 2)}\n\n` +
  `export const persianTranslations: Record<string, string> = ${JSON.stringify(persian, null, 2)}\n`,
  "utf8",
);
console.log(`Generated ${sortedPhrases.length} phrases in ${outputFile}`);
